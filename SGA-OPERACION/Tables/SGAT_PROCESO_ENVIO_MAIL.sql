  IS 'MENSAJE DE CORREO.';
COMMENT ON COLUMN OPERACION.SGAT_PROCESO_ENVIO_MAIL.PEMN_INTENTO
  IS 'NÚMERO DE INTENTOS PARA ENVIAR EL CORREO.';
COMMENT ON COLUMN OPERACION.SGAT_PROCESO_ENVIO_MAIL.PEMN_ESTMSJ
  IS 'ESTADO DEL MENSAJE. 1-ACTIVO,0-INACTIVO';
COMMENT ON COLUMN OPERACION.SGAT_PROCESO_ENVIO_MAIL.PEMV_USUREG
  IS 'USUARIO DE REGISTRO.';
COMMENT ON COLUMN OPERACION.SGAT_PROCESO_ENVIO_MAIL.PEMD_FECREG
