-- CREATE TABLE
CREATE TABLE OPERACION.SGAT_PROCESO_ENVIO_MAIL
(
  PEMN_IDMSJ   NUMBER(3) NOT NULL,
  PEMV_ASUNTO  VARCHAR2(200) NOT NULL,
  PEMV_CUERPO  VARCHAR2(1000) NOT NULL,
  PEMN_INTENTO NUMBER(1) NOT NULL,
  PEMN_ESTMSJ  NUMBER(1) NOT NULL,
  PEMV_USUREG  VARCHAR2(30) NOT NULL,
  PEMD_FECREG  DATE NOT NULL,
  PEMV_USUMOD  VARCHAR2(30),
  PEMD_FECMOD  DATE,
  PEMV_FROM    VARCHAR2(200) NOT NULL
)
TABLESPACE OPERACION_DAT
  PCTFREE 10
  INITRANS 1
  MAXTRANS 255
  STORAGE
  (
    INITIAL 64K
    NEXT 1M
    MINEXTENTS 1
    MAXEXTENTS UNLIMITED
  );
-- ADD COMMENTS TO THE COLUMNS 
