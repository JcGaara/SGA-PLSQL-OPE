  IS 'ESTADO DE EQUIPO DE ACCESO.';
COMMENT ON COLUMN OPERACION.SGAT_EF_EQUIPO_SRVC.EFEQV_USUREG
  IS 'USUARIO DE REGISTRO.';
COMMENT ON COLUMN OPERACION.SGAT_EF_EQUIPO_SRVC.EFEQD_FECREG
  IS 'FECHA DE REGISTRO.';
COMMENT ON COLUMN OPERACION.SGAT_EF_EQUIPO_SRVC.EFEQV_USUMOD
  IS 'USUARIO DE MODIFICACIÓN.';
COMMENT ON COLUMN OPERACION.SGAT_EF_EQUIPO_SRVC.EFEQD_FECMOD
  IS 'FECHA DE MODIFICACIÓN.';
