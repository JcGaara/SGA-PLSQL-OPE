COMMENT ON COLUMN OPERACION.MAESTRO_SERIES_EQU.SA IS 'Tipo de Stock';

COMMENT ON COLUMN OPERACION.MAESTRO_SERIES_EQU.EQUIPO IS 'Equipo';

COMMENT ON COLUMN OPERACION.MAESTRO_SERIES_EQU.IDALM IS 'Identificador PK Tabla';

COMMENT ON COLUMN OPERACION.MAESTRO_SERIES_EQU.CODUSU IS 'Usuario que registro';

COMMENT ON COLUMN OPERACION.MAESTRO_SERIES_EQU.FECUSU IS 'Fecha de registro';

