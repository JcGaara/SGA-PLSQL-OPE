CREATE TABLE OPERACION.MAESTRO_SERIES_EQU
(
  COD_SAP        VARCHAR2(20 BYTE),
  NROSERIE       VARCHAR2(100 BYTE),
  CENTRO         VARCHAR2(10 BYTE),
  ALMACEN        VARCHAR2(10 BYTE),
  SA             VARCHAR2(10 BYTE),
  EQUIPO         VARCHAR2(10 BYTE),
  IDALM          NUMBER                         NOT NULL,
  CODUSU         VARCHAR2(30 BYTE)              DEFAULT user,
  FECUSU         DATE                           DEFAULT sysdate,
  FLG_PROCESO    NUMBER                         DEFAULT 1,
  DESPACHADO     NUMBER                         DEFAULT 0,
  MAC1           VARCHAR2(30 BYTE),
  MAC2           VARCHAR2(30 BYTE),
  MAC3           VARCHAR2(30 BYTE),
  MAC4           VARCHAR2(30 BYTE),
  MAC5           VARCHAR2(30 BYTE),
  DISPONIBLE     NUMBER,
  MODELOEQUITW   VARCHAR2(150 BYTE),
  ESTADOITW      NUMBER(2),
  FECUSUMOD      DATE,
  CODUSUMOD      VARCHAR2(30 BYTE),
  ID_PRODUCTO    VARCHAR2(30 BYTE),
  FASE           NUMBER(1),
  ID_ACTIVACION  VARCHAR2(32 BYTE)
